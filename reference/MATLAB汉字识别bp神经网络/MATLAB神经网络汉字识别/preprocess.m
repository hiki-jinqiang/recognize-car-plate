%preprocess
p1=ones(64,64);
bw=im2bw(X,0.5);%转换成二值图像
%用矩形框截取图像
[i,j]=find(bw==0);
imin=min(i);
imax=max(i);
jmin=min(j);
jmax=max(j);
bw1=bw(imin:imax,jmin:jmax);
%调整比例，变换成64*64图像
rate=64/max(size(bw1));
bw1=imresize(bw1,rate);
[i,j]=size(bw1);
i1=round((64-i)/2);
j1=round((64-j)/2);
p1(i1+1:i1+i,j1+1:j1+j)=bw1;
p1=-1.*p1+ones(64,64);
%显示预处理的结果
axes(h2);
imshow(p1);